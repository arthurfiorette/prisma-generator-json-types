import { expectAssignable, expectNotAssignable } from 'tsd';
import { Model } from '../target/mongo/index';

declare global {
  export namespace PMongoJson {
    export type Simple = 1;
    export type Optional = 2;
    export type List = 3;
  }
}

expectAssignable<Model>({
  id: '0',
  simple: 1,
  optional: 2,
  list: [3]
});

expectAssignable<Model>({
  id: '0',
  simple: 1,
  optional: null,
  list: [3]
});

expectAssignable<Model>({
  id: '0',
  simple: 1,
  optional: null,
  list: []
});

expectAssignable<Model>({
  id: '0',
  simple: 1,
  optional: 2,
  list: [3, 3, 3]
});

expectNotAssignable<Model>({
  id: '0',
  simple: '1',
  optional: 2,
  list: [3]
});

expectNotAssignable<Model>({
  id: '0',
  simple: 1,
  optional: '2',
  list: [3]
});

expectNotAssignable<Model>({
  id: '0',
  simple: 1,
  optional: 'undefined',
  list: 3
});

expectNotAssignable<Model>({
  id: '0',
  simple: 1,
  optional: 2,
  list: '3,3,3'
});
